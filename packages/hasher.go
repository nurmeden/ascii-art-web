package packages

import (
	"crypto/sha256"
)

func CheckerHash(str []byte) bool {
	ARROFHASH := [][]byte{
		{225, 148, 241, 3, 52, 66, 97, 122, 184, 167, 142, 28, 166, 58, 32, 97, 245, 204, 7, 163, 240, 90, 194, 38, 237, 50, 235, 157, 253, 34, 166, 191},
		{38, 185, 77, 11, 19, 75, 119, 233, 253, 35, 224, 54, 11, 253, 129, 116, 15, 128, 251, 127, 101, 65, 209, 216, 197, 216, 94, 115, 238, 85, 15, 115},
		{71, 44, 250, 176, 86, 116, 6, 209, 3, 19, 171, 34, 32, 170, 94, 100, 183, 251, 241, 121, 109, 150, 193, 28, 119, 82, 70, 115, 45, 206, 126, 122},
	}
	h := sha256.New()
	h.Write(str)
	for _, value := range ARROFHASH {
		if string(value) == string(h.Sum(nil)) {
			return true
		}
	}
	return false
}
